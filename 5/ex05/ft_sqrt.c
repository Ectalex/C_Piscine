#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	k;

	k = 1;
	if (nb > 2147395600)
		return (0);
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while ((k * k) <= nb)
	{
		if ((k * k) == nb)
			return (k);
		k++;
	}
	return (0);
}
