#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	if (nb == 1 || nb == 0)
		return (0);
	while (i <= 46340 && i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
